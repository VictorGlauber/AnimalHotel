    private void preencherFiltros() {
        preencherFiltroEspecie();
        preencherFiltroEstado();
        preencherFiltroPorte();

    }

    private void preencherFiltroEspecie() {
        filtroEspecie.removeAllItems();
        filtroEspecie.addItem("Todos");
        for (Especie e : Especie.values()) {
            filtroEspecie.addItem(e);
        }
    }

    private void preencherFiltroPorte() {

        filtroPorte.removeAllItems();
        filtroPorte.addItem("Todos");
        for (Porte e : Porte.values()) {
            filtroPorte.addItem(e);
        }

    }

    private void preencherFiltroEstado() {

        filtroEstado.removeAllItems();
        filtroEstado.addItem("Todos");
        for (AcomodacaoEstado e : AcomodacaoEstado.values()) {
            filtroEstado.addItem(e);
        }

    }

    private boolean checarNoFiltro(Acomodacao acomodacao) {
        if (filtroEstado.getSelectedItem()!=null && filtroEstado.getSelectedItem() != "Todos") {
            String e = (String) filtroEstado.getSelectedItem().toString();
            if (e != null && !e.equals(acomodacao.getEstado().toString())) {
                return false;
            }
        }
        if (filtroPorte.getSelectedItem()!=null && filtroPorte.getSelectedItem() != "Todos") {
            String p = (String) filtroPorte.getSelectedItem().toString();
            if (p != null && !p.equals(acomodacao.getDimensao().toString())) {
                return false;
            }
        }
        if (filtroEspecie.getSelectedItem()!=null && filtroEspecie.getSelectedItem() != "Todos") {
            String es = (String) filtroEspecie.getSelectedItem().toString();
            if (es != null && !es.equals(acomodacao.getEspecie().toString())) {
                return false;
            }
        }
        return true;
    }

    private void preencherAcomodacoes() {
        acomodacoesStatus.setText("");
        Set<Acomodacao> aco = BancoDeDados.getInstance().getAcomodacoes();
        Iterator<Acomodacao> acomodacoes = aco.iterator();
        while (acomodacoes.hasNext()) {

            Acomodacao acomodacao = acomodacoes.next();
            if (checarNoFiltro(acomodacao)) {
                acomodacoesStatus.setText(acomodacoesStatus.getText()
                        + acomodacao.getNum() + " : " + acomodacao.getDimensao() + "; "
                        + acomodacao.getEstado() + "; ");
                if (acomodacao.getEstado() == AcomodacaoEstado.emUso) {
                    acomodacoesStatus.setText(acomodacoesStatus.getText()
                            + acomodacao.getEstadia().getAnimal().getNome() + "\n----------------------------\n");
                } else {
                    acomodacoesStatus.setText(acomodacoesStatus.getText() + "\n----------------------------\n");
                }
            }
        }
    }

